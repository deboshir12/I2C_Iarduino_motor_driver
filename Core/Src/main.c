static void UART_SendText(const char *text)
{
  uint16_t len = 0U;

  while (text[len] != '\0')
  {
    len++;
  }

  (void)HAL_UART_Transmit(&huart1, (const uint8_t *)text, len, 50U);
}

static void UART_SendMotorStatus(const char *command_name, HAL_StatusTypeDef status)
{
  char text[40];

  if (status == HAL_OK)
  {
    (void)snprintf(text, sizeof(text), "OK %s\r\n", command_name);
  }
  else
  {
    (void)snprintf(text,
                   sizeof(text),
                   "ERR %s st=%u i2c=0x%08lX\r\n",
                   command_name,
                   (unsigned int)status,
                   (unsigned long)HAL_I2C_GetError(&hi2c1));
  }

  UART_SendText(text);
}

static void HandleUartCommand(const UartCommand *command)
{
  HAL_StatusTypeDef status = HAL_OK;

  switch (command->type)
  {
    case UART_COMMAND_TYPE_PWM:
      status = Motor_SetPwm(&hi2c1, command->value);
      UART_SendMotorStatus("pwm", status);
      break;

    case UART_COMMAND_TYPE_RPM:
      status = Motor_SetRpm(&hi2c1, command->value);
      UART_SendMotorStatus("rpm", status);
      break;

    case UART_COMMAND_TYPE_INVALID:
      status = HAL_ERROR;
      UART_SendText("ERR cmd\r\n");
      break;

    default:
      break;
  }

  last_motor_status = status;
  last_uart_command = command->type;
  last_uart_value = command->value;
  last_motor_error = HAL_I2C_GetError(&hi2c1);
}

